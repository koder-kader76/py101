# Type Conversions

# str()
print(str(True))
# True

# using print()
number = 42
print("The answer is:", number)
# The answer is: 42

# String interpolation
name = "John"
age = 30
message = f"Hello, my name is {name} and I am {age} years old."
print(message)
# Hello, my name is John and I am 30 years old.